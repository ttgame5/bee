let currentIndex = 0;
const wordsPerPage = 5;

const wordDisplay = document.getElementById('word-display');
const prevBtn = document.getElementById('prev-btn');
const nextBtn = document.getElementById('next-btn');

function displayWords() {
    wordDisplay.innerHTML = ''; // Clear previous words
    const startIndex = currentIndex;
    const endIndex = Math.min(currentIndex + wordsPerPage, tamilWords.length);

    for (let i = startIndex; i < endIndex; i++) {
        const p = document.createElement('p');
        p.textContent = tamilWords[i];
        wordDisplay.appendChild(p);
    }

    // Enable/disable buttons
    prevBtn.disabled = currentIndex === 0;
    nextBtn.disabled = currentIndex + wordsPerPage >= tamilWords.length;
}

prevBtn.addEventListener('click', () => {
    if (currentIndex > 0) {
        currentIndex -= wordsPerPage;
        displayWords();
    }
});

nextBtn.addEventListener('click', () => {
    if (currentIndex + wordsPerPage < tamilWords.length) {
        currentIndex += wordsPerPage;
        displayWords();
    }
});

// Initial display of words
displayWords();
